/* AULA DESCODIFICADAS  INTERVALO 2 - ESTRUTURA DE REPETIÇÃO
LOOP contando de 0 a 10; 10 a 0; 
*/ 
let numeroSoma = 0;
while (numeroSoma <= 10) {
    console.log(numeroSoma);
    numeroSoma++;
}

let numeroSubtracao = 10;
while (numeroSubtracao >= 0) {
    console.log(numeroSubtracao);
    numeroSubtracao--;
}
console.log ("Terminei de contar!")